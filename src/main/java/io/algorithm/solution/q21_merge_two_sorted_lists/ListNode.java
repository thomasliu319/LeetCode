package io.algorithm.solution.q21_merge_two_sorted_lists;

/**
 * @Author: jian.liu
 * @Description //链表节点
 * @Date: 2022/1/26 16:30
 */
public class ListNode {

    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}
